#include<iostream>
using namespace std;
int main(){
   int x=3;
   cout<<x<<endl;   //this is value of x
   cout<<&x<<endl; //this is address of x where it is place in memory
   float y=5;
   cout<<&y<<endl;   //this is address of y
   char z='S';
   cout<<&z<<endl;
   return 0;
}
